use crate::app_state::cookie_value;
use axum::http::HeaderMap;
use std::str::FromStr;

#[derive(Clone, Copy, PartialEq, Eq)]
pub(crate) enum Locale {
    En,
    Ru,
    Zh,
    Ko,
    Ja,
}

impl Locale {
    pub(crate) fn code(self) -> &'static str {
        match self {
            Locale::En => "en",
            Locale::Ru => "ru",
            Locale::Zh => "zh",
            Locale::Ko => "ko",
            Locale::Ja => "ja",
        }
    }

    pub(crate) fn label(self) -> &'static str {
        match self {
            Locale::En => "EN",
            Locale::Ru => "RU",
            Locale::Zh => "CN",
            Locale::Ko => "KR",
            Locale::Ja => "JP",
        }
    }

    pub(crate) fn lang_attr(self) -> &'static str {
        match self {
            Locale::En => "en",
            Locale::Ru => "ru",
            Locale::Zh => "zh",
            Locale::Ko => "ko",
            Locale::Ja => "ja",
        }
    }

    pub(crate) fn all() -> &'static [Locale] {
        &[Locale::En, Locale::Ru, Locale::Zh, Locale::Ko, Locale::Ja]
    }
}

impl FromStr for Locale {
    type Err = ();
    fn from_str(s: &str) -> Result<Self, Self::Err> {
        match s.trim().to_lowercase().as_str() {
            "en" | "en_us" | "en-us" => Ok(Locale::En),
            "ru" | "ru_ru" | "ru-ru" => Ok(Locale::Ru),
            "zh" | "cn" | "zh_cn" | "zh-cn" => Ok(Locale::Zh),
            "ko" | "kr" | "ko_kr" | "ko-kr" => Ok(Locale::Ko),
            "ja" | "jp" | "ja_jp" | "ja-jp" => Ok(Locale::Ja),
            _ => Err(()),
        }
    }
}

pub(crate) fn locale_from_headers(headers: &HeaderMap) -> Locale {
    if let Some(value) = cookie_value(headers, "gear_lang") {
        if let Ok(locale) = Locale::from_str(&value) {
            return locale;
        }
    }
    Locale::En
}

pub(crate) fn t(locale: Locale, key: &str) -> &str {
    match locale {
        Locale::En => t_en(key),
        Locale::Ru => t_ru(key),
        Locale::Zh => t_zh(key),
        Locale::Ko => t_ko(key),
        Locale::Ja => t_ja(key),
    }
}

